int	dollar_special_var(char c)
{
	if (c == '?' || c == '#' || c == '&' || c == '\0' || c == '.' \
		|| c == ',' || c == '=' || c == '+' || c == '-' || c == '%' \
		|| c == '!' || c == '/' || c == '0' || c == ':' || c == ';' \
		|| c == '<' || c == ')' || c == '>' || c == ']' || c == '}' \
		|| c == '\\' || c == '^' || c == '_' || c == '~' || c == '{' \
		|| c == '[')
		return (1);
	return (0);
}
